// LITERAL SUFFIXES
// PREFERABLY IN UPPERCASE
// Can be used with prefixes

#include<iostream>
using namespace std;

int main()
{
    int a = 124;
    unsigned int b = 124U;
    long int c = 124L;
    long long d = 124LL;
    cout << a << "\n";
    cout << b << "\n";
    cout << c << "\n";
    cout << d << "\n";
    return 0;
}